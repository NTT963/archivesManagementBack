package org.jit.sose.entity;

public class IconInfo {
	
	private int ID;
	private String iconTitle;
	private String iconPicUrl;
	private String navPath;
	private String page;
	private String iconOrder;
	private String roleID;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getIconTitle() {
		return iconTitle;
	}
	public void setIconTitle(String iconTitle) {
		this.iconTitle = iconTitle;
	}
	public String getIconPicUrl() {
		return iconPicUrl;
	}
	public void setIconPicUrl(String iconPicUrl) {
		this.iconPicUrl = iconPicUrl;
	}
	public String getNavPath() {
		return navPath;
	}
	public void setNavPath(String navPath) {
		this.navPath = navPath;
	}
	public String getPage() {
		return page;
	}
	public void setPage(String page) {
		this.page = page;
	}
	public String getIconOrder() {
		return iconOrder;
	}
	public void setIconOrder(String iconOrder) {
		this.iconOrder = iconOrder;
	}
	public String getRoleID() {
		return roleID;
	}
	public void setRoleID(String roleID) {
		this.roleID = roleID;
	}
	public IconInfo() {
	}
	
	

}
